    <h2><b>Unidad Agrícola Familiar</b></h2>
    <p><i>"Se entiende por unidad agrícola familiar (UAF), la empresa básica de producción agrícola, pecuaria, acuícola o forestal cuya extensión, conforme a las condiciones agroecológicas de la zona y con tecnología adecuada, permite a la familia remunerar su trabajo y disponer de un excedente capitalizable que coadyuve a la formación de su patrimonio. La UAF no requerirá normalmente para ser explotada sino del trabajo del propietario y su familia, sin perjuicio del empleo de mano de obra extraña, si la naturaleza de la explotación así lo requiere"</i>. (Art 38/94)</p>

